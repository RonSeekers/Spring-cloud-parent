SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE  if EXISTS user;
CREATE TABLE USER(
id int(11) NOT NULL AUTO_INCREMENT ,
username VARCHAR(255) ,
name VARCHAR (255),
age int(3),
balance DECIMAL(10,2) ,
PRIMARY KEY (id)
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8;
SET FOREIGN_KEY_CHECKS = 1;